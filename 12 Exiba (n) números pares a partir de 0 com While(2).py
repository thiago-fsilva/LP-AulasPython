n = int(input())        # n | par
x = 0                   # 1    0 --> 2*(1-1)
                        # 2    2 --> 2*(2-1)
while x <= 2*(n-1):     # 3    4 --> 2*(3-1)
    print(x)            # 4    6 --> 2*(4-1)
    x += 2              # .    . -->   ...
print('FIM')            # n    p --> 2*(n-1)
